package chanhbc.com.zingmp3.model;


public class ItemListMusic {
    private int icon;
    private String name;

    public ItemListMusic(int icon, String name){
        this.icon = icon;
        this.name = name;
    }

    public int getIcon() {
        return icon;
    }

    public String getName() {
        return name;
    }
}
